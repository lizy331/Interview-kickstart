package InterviewByCompany.Apple;

public class ListNode {
    Integer val;

    ListNode next;

    public ListNode(Integer value){
        this.val = value;
    }

    public Integer getVal() {
        return val;
    }

    public void setVal(Integer val) {
        this.val = val;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
